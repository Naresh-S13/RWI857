package Oops;

public class Encapsulation {
    private int id;
    private String name;
    public void setId(int id){
        this.id=id;
    }
    public int getId(){
        return this.id;
    }
    void setName(String name){
        this.name=name;
    }
    String getName(){
        return this.name;
    }
}
